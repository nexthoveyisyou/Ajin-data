## ---------------------------------------------------------------------
## [1] str.isdecima() : 10진수 숫자 문자만 허용
##                      세 함수 중 가장 좁은 범위
##                      가  능 : 0~9
##                      불가능 : 제곱숫자(²), 분수문자(½), 로마숫자(Ⅳ), 
##                              한자 수(四), 음수(-), 소수점(.), 
##                              슬래시 분수(1/2), 콤마(1,000)
## ---------------------------------------------------------------------
print("\n==> str.isdecimal() ")
print(f'123     : {"123".isdecimal()}')     # True
print(f'²       : {"²".isdecimal()}')       # False
print(f'½       : {"½".isdecimal()}')       # False
print(f'Ⅳ       : {"Ⅳ".isdecimal()}')     # False
print(f'四      : {"四".isdecimal()}')      # False
print(f'3.14    : {"3.14".isdecimal()}')    # False
print(f'-10     : {"-10".isdecimal()}')     # False
print(f'1,000   : {"1,000".isdecimal()}')   # False

## ---------------------------------------------------------------------
## [2] str.isdigit() :  10진수 숫자 문자 + 일부 digit 문자까지 허용
##                      isdecimal()보다 넓은 범위
##                      가  능 : 0~9, 제곱숫자(²)
##                      불가능 : 분수문자(½), 로마숫자(Ⅳ), 한자 수(四),
##                              음수(-), 소수점(.), 슬래시 분수(1/2), 콤마(1,000)
## ---------------------------------------------------------------------
print("\n==> str.isdigit() ")
print(f'123     : {"123".isdigit()}')     # True
print(f'²       : {"²".isdigit()}')       # True
print(f'½       : {"½".isdigit()}')       # False
print(f'Ⅳ       : {"Ⅳ".isdigit()}')     # False
print(f'四      : {"四".isdigit()}')      # False
print(f'3.14    : {"3.14".isdigit()}')    # False
print(f'-10     : {"-10".isdigit()}')     # False
print(f'1,000   : {"1,000".isdigit()}')   # False

## ---------------------------------------------------------------------
## [2] str.isnumeric() : 숫자값을 가진 문자 전반을 허용
##                       세 함수 중 가장 넓은 범위
##                       가  능 : 0~9, 제곱숫자(²), 일부 분수 문자(½), 
##                               로마숫자(Ⅳ), 한자 수(四)
##                       불가능 : 음수(-), 소수점(.), 슬래시 분수(1/2), 콤마(1,000)
## ---------------------------------------------------------------------
print("\n==> str.isnumeric() ")
print(f'123     : {"123".isnumeric()}')     # True
print(f'²       : {"²".isnumeric()}')       # True
print(f'½       : {"½".isnumeric()}')       # True
print(f'Ⅳ       : {"Ⅳ".isnumeric()}')     # True
print(f'四      : {"四".isnumeric()}')      # True
print(f'1/2     : {"1/2".isnumeric()}')     # False
print(f'3.14    : {"3.14".isnumeric()}')    # False
print(f'-10     : {"-10".isnumeric()}')     # False
print(f'1,000   : {"1,000".isnumeric()}')   # False