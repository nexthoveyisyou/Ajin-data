## ==============================================================
## 함수 사용 즉, 함수 호출 : 함수이름(전달데이터_인자)
## ==============================================================
print( add_two_int(10, 20) )
#print( add_two_int() )                     ## ERROR :  전달 받은 데이터 없어서 오류

print( add_two_int_default(17, 9) )         ## num1 = 17,  num2 = 9
print( add_two_int_default() )              ## 기본값 매개변수로 정상 동작
print( add_two_int_default(5) )             ## 전달된 값이 있으면 매개변수에 저장, 없으면 기본값 사용