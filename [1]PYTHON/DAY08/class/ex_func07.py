##
## -> 예외            : 동일 기능의 코드를 데이터 개수에 따라 여러 개 정의
##                     => 개선 : 가변인자        *매개변수 
##                              키워드파라미터  **매개변수 
## ==============================================================
## --------------------------------------------------------------
## 함수이름 : add_two_int
## 함수기능 : 2개의 정수 덧셈 후 결과 반환
## 매개변수 : num1, num2
## 함수결과 : 덧셈 값
## --------------------------------------------------------------
def add_two_int(num1, num2):
    value = num1 + num2
    return value

## --------------------------------------------------------------
## 함수이름 : add_two_int_default
## 함수기능 : 2개의 정수 덧셈 후 결과 반환
##           전달 받은 데이터가 없으면 int 기본값 0으로 처리함
## 매개변수 : num1=0, num2=0
## 함수결과 : 덧셈 값
## --------------------------------------------------------------
def add_two_int_default( num1=0, num2=0):
    print(f'num1 : {num1},  num2 : {num2}', end='==>')
    value = num1 + num2
    return value


## --------------------------------------------------------------
