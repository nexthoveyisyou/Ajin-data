s = x.items()
print(s)

## keys : 키를 모두 가져옴
s = x.keys()
print(s)

## values : 값을 모두 가져옴
s = x.values()
print(s)

## 9. 리스트와 튜플로 딕셔너리 만들기

## dict.fromkeys(키리스트) : 키 리스트로 딕셔너리를 생성하며 값은 모두 None으로 저장
keys = ['a', 'b', 'c', 'd']
x = dict.fromkeys(keys)
print(x)

## dict.fromkeys(키리스트,값) : 해당 값이 키의 값으로 저장됨
y = dict.fromkeys(keys, 100)
print(y)

