## 26.2 집합 연산 사용하기
## ----------------------------------------------------------------
## 집합 연산은 산술 연산자와 논리 연산자를 활용

## 합집합 : union , or 연산자 : |

a = {1, 2, 3, 4}
b = {3, 4, 5, 6}

print(a | b)
print(set.union(a,b))

## 교집합 : intersection, and 연산자 : &

print(a & b)
print(set.intersection(a, b))

## 차집합 : difference,  뺄셈 연산자 : -

print(a - b)
print(set.difference(a, b))

## 대칭 차집합 : symmetric_difference,  XOR 연산자 : ^

print(a^b)
print(set.symmetric_difference(a, b)) # 같은 값 3,4를 제외한 다른 모든 요소

## 1. 집합 연산 후 할당 연산자 사용하기

## 세트 합침 : update,  연산자 : |=

a = {1, 2, 3, 4}
a |= {5}
print(a)

a = {1, 2, 3, 4}
a.update({5})
print(a)

## 다른 세트와 겹치는 요소를 현재 세트에 저장 : intersection_update,  연산자 : &=

a = {1, 2, 3, 4}
a &= {0, 1, 2, 3, 4}
print(a)

a = {1, 2, 3, 4}
a.intersection_update({0, 1, 2, 3, 4})
print(a)

## 현재 세트에서 다른 세트를 뺌 : difference_update,  연산자 : -=

a = {1, 2, 3, 4}
a -= {3}
print(a)

a = {1, 2, 3, 4}
a.difference_update({3})
print(a)

## 현재 세트와 다른 세트 중에서 겹치지 않는 요소만 현재 세트에 저장 : symmetric_difference_update,  연산자 : ^=

a = {1, 2, 3, 4}
a ^= {3, 4, 5, 6}
print(a)

a = {1, 2, 3, 4}
a.symmetric_difference_update({3, 4, 5, 6})
print(a)

## 2. 부분 집합과 상위 집합 확인하기

## 현재 세트가 다른 세트의 부분집합인지 확인 : issubset,  연산자 : <=

a = {1, 2, 3, 4}

print(a <= {1, 2, 3, 4})
print(a.issubset({1, 2, 3, 4, 5}))

## 현재 세트가 다른 세트의 진부분집합(현재 세트 < 다른 세트)인지 확인 : proper subset,  연산자 : <

a = {1, 2, 3, 4}
print(a < {1, 2, 3, 4, 5})

## 현재 세트가 다른 세트의 상위집합(현재 세트 >= 다른 세트)인지 확인 : superset,  연산자 : >=

a = {1, 2, 3, 4}
print(a >= {1, 2, 3, 4})
print(a.issuperset({1, 2, 3, 4}))

## 현재 세트가 다른 세트의 진상위집합(현재 세트 > 다른 세트)인지 확인 : proper superset,  연산자 : >

a = {1, 2, 3, 4}
print(a > {1, 2, 3})

## 3. 세트가 같은지 다른지 확인하기

## == 연산자 : 서로 같은지 확인 / 같으면 True
a = {1, 2, 3, 4}
print(a == {1, 2, 3, 4})
print(a == {4, 2, 1, 3})

## != 연산자 : 세트가 다른지 확인 / 다르면 True
a = {1, 2, 3, 4}
print(a != {1, 2, 3})

## 4. 세트가 겹치지 않는지 확인하기

## disjoint : 현재 세트가 다른 세트와 겹치지 않는지 확인 / 겹치는 요소가 없으면 True, 있으면 False
a = {1, 2, 3, 4}
print(a.isdisjoint({5, 6, 7, 8})) # 겹치는 요소 없음
print(a.isdisjoint({3, 4, 5, 6})) # a와 3,4가 겹침

