## 1. 딕셔너리에 키-값 쌍 추가하기
## 딕셔너리에 키 - 값 쌍 추가하는 메서드 
#  1) setdefault : 키 - 값 쌍 추가
#  2) update : 키의 값 수정, 키가 없으면 키-값 쌍 추가

## 2. 딕셔너리에 키와 기본값 저장하기

## setdefault(키) : 키만 지정하면 값에 None을 저장함
x = {'a' : 10 , 'b' : 20 , 'c' : 30 , 'd' : 40}
x.setdefault('e')
print(x)

## setdefault(키,값) : 키와 기본값을 지정하면 값에 기본값을 지정한 뒤 해당 값을 반환
x.setdefault('f', 100)
print(x)

## 3. 딕셔너리에서 키의 값 수정하기

## update(키 = 값) : 딕셔너리에서 키의 값을 수정
x = {'a' : 10 , 'b' : 20 , 'c' : 30 , 'd' : 40}
x.update(e = 90)
print(x)

## update는 키-값 쌍 여러개를 콤마로 구분하면 값을 한꺼번에 수정 가능
x.update(a = 900, f=60)
print(x)

## update(키 = 값)은 키가 문자열일 때만 사용 가능, 만약 키가 숫자일 경우 update(딕셔너리)처럼 딕셔너리를 넣어서 값을 수정
y = {1 : 'one', 2 : 'two'}
y.update({1 : 'ONE', 3: 'THREE'})
print(y)

## update(리스트), update(튜플) : 키와 값을 리스트(튜플)로 만들고 이 리스트를 다시 리스트 안에 넣어서 키-값 쌍을 나열
y.update([[2, 'TWO'], [4, 'FOUR']])
print(y)

## update(반복 가능한 객체): 키-값 쌍으로 된 반복 가능한 객체로 값을 수정함
y.update(zip([1,2], ['one', 'two']))
print(y)

## 4. 딕셔너리에서 키-값 쌍 삭제하기

## pop(키) : 딕셔너리에서 특정 키-값 쌍을 삭제한 뒤 삭제한 값을 반환
x = {'a' : 10 , 'b' : 20 , 'c' : 30 , 'd' : 40}
x.pop('a')
print(x)

## pop(키, 기본값) : 기본값을 지정하면 딕셔너리에 키가 있을 때는 해당 키-값 쌍을 삭제한 뒤 삭제한 값을 반환 / 키가 없을 때는 기본 값 0 반환
s = x.pop('z', 0)
print(s)

## del[] : 특정 키-값 쌍을 삭제, 키 값을 지정하여 삭제
x = {'a': 10, 'b': 20, 'c': 30, 'd': 40}
del x['a']
print(x)

## 5. 딕셔너리에서 임의의 키-값 쌍 삭제하기

## popitem() : 딕셔너리에서 임의의 키-값 쌍을 삭제한 뒤 삭제한 키-값 쌍을 튜플로 반환
x = {'a': 10, 'b': 20, 'c': 30, 'd': 40}
s = x.popitem()
print(s)
print(x)

## 6. 딕셔너리의 모든 키-값 쌍을 삭제하기

## clear() : 딕셔너리의 모든 키-값 쌍을 삭제
x = {'a': 10, 'b': 20, 'c': 30, 'd': 40}
x.clear()
print(x)

## 7. 딕셔너리에서 키의 값을 가져오기

## get(키) : 딕셔너리에서 특정 키의 값을 가져옴
x = {'a': 10, 'b': 20, 'c': 30, 'd': 40}
s = x.get('a')
print(s)

## get(키,기본값) : 기본값을 지정하면 딕셔너리에 키가 있을 때는 해당 키의 값을 반환하지만 키가 없을 때는 기본값을 반환
s = x.get('z', 0)
print(s)

## 8. 딕셔너리에서 키-값 쌍을 모두 가져오기

## items : 키 - 값 쌍을 모두 가져옴
