## =========================================================================
## 파일 입출력 - 파일 쓰기
## =========================================================================
## -------------------------------------------------------------------------
## 동작 프로세스
## (1) 파일 열기 -- 쓰기 모드 , 인코딩 방식 설정 체크!!
## (2) 내용 쓰기
## (3) 파일 닫기
## -------------------------------------------------------------------------
## ---------------------
## 전역 변수
## ---------------------
FILE_PATH = './mydata.txt'

## --------------------------------------
## 파일 쓰기 - 1 : w모드
## 존재하지 않는 파일이면 생성
## 존재하는 파일이면 내용 지우고 쓰기
## --------------------------------------
## (1) 열기 - open()
f = open(FILE_PATH, mode='wt',encoding='utf8')   # 한글 인코딩

## (2) 쓰기
#  => 모든 내용 읽어오기 
wCnt = f.write("Good Luck 좋은 날!!!\n")
print(f'쓰기한 개수 : {wCnt}개')

wCnt = f.write("1234567890")
print(f'쓰기한 개수 : {wCnt}개')

## (3) 닫기
f.close()
