if (len(words)<n):
    print('wrong')
else:
    ## N-gram은 리스트 표현식을 사용하고, 결과는 튜플로 출력하라고 했으므로 
    ## 리스트 표현식으로 문자열 리스트를 만든 뒤 zip에 넣어주어야 합니다.
    n_gram = zip(*[words[i:] for i in range(n)])                                
    for i in n_gram:
        print(i)




