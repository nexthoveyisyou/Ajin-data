## ----------------------------------------------------------------------

## ----------------------------------------------------------------------
## 27.1.1  파일에 문자열 쓰기
## ----------------------------------------------------------------------

file = open('hello.txt', 'w')    # hello.txt 파일을 쓰기 모드(w)로 열기. 파일 객체 반환
file.write('Hello, world!')      # 파일에 문자열 저장
file.close()                     # 파일 객체 닫기

# => 파일 열기 -> 파일 쓰기 -> 파일 닫기 순서

## ----------------------------------------------------------------------
## 27.1.2 파일에서 문자열 읽기
## ----------------------------------------------------------------------

file = open('hello.txt', 'r')    # hello.txt 파일을 읽기 모드(r)로 열기. 파일 객체 반환
s = file.read()                  # 파일에서 문자열 읽기
print(s)                         # Hello, world!
file.close()                     # 파일 객체 닫기

# => 파일 열기 => 파일 읽기 => 파일 닫기

## ----------------------------------------------------------------------
## 27.1.3 자동으로 파일 객체 닫기
## with open(파일이름, 파일모드) as 파일객체:
##     코드
## => with as는 파일 객체를 자동으로 닫아줌
## ----------------------------------------------------------------------

with open('hello.txt', 'r') as file:    # hello.txt 파일을 읽기 모드(r)로 열기
    s = file.read()                     # 파일에서 문자열 읽기
    print(s)                            # Hello, world!

## ----------------------------------------------------------------------
## 27.2 문자열 여러 줄을 파일에 쓰기, 읽기
## ----------------------------------------------------------------------

## ----------------------------------------------------------------------
## 27.2.1  반복문으로 문자열 여러 줄을 파일에 쓰기
## ----------------------------------------------------------------------

with open('hello.txt', 'w') as file:    # hello.txt 파일을 쓰기 모드(w)로 열기
    for i in range(3):
        file.write('Hello, world! {0}\n'.format(i)) #줄바꿈 반드시 필요

## ----------------------------------------------------------------------
## 27.2.2  리스트에 들어있는 문자열을 파일에 쓰기
## ----------------------------------------------------------------------
lines = ['안녕하세요.\n', '파이썬\n', '코딩 도장입니다.\n']
 
with open('hello.txt', 'w') as file:    # hello.txt 파일을 쓰기 모드(w)로 열기
    file.writelines(lines)  # writelines는 리스트에 들어있는 문자열을 파일에 씁니다

## ----------------------------------------------------------------------
## 27.2.3  리스트에 들어있는 문자열을 파일에 쓰기
## ----------------------------------------------------------------------

with open('hello.txt', 'r') as file:    # hello.txt 파일을 읽기 모드(r)로 열기
    lines = file.readlines() # readlines는 파일의 내용을 한 줄씩 리스트 형태로 가져옵니다.
    print(lines) 

## ----------------------------------------------------------------------
## 27.2.4  파일의 내용을 한 줄씩 읽기
## ----------------------------------------------------------------------

with open('hello.txt', 'r') as file:    # hello.txt 파일을 읽기 모드(r)로 열기
    line = None    # 변수 line을 None으로 초기화
    while line != '':
        line = file.readline()  #readline으로 파일을 읽을 때는 while 반복문을 활용
        print(line.strip('\n'))    # 파일에서 읽어온 문자열에서 \n 삭제하여 출력

## ----------------------------------------------------------------------
## 27.2.5  for 반복문으로 파일의 내용을 줄 단위로 읽기
## ----------------------------------------------------------------------

with open('hello.txt', 'r') as file:    # hello.txt 파일을 읽기 모드(r)로 열기
    for line in file:    # for에 파일 객체를 지정하면 파일의 내용을 한 줄씩 읽어서 변수에 저장함
        print(line.strip('\n'))    # 파일에서 읽어온 문자열에서 \n 삭제하여 출력

