def calculate(a,op,b):
    if op == "+":
        return a + b
    elif op == "-":
        return a - b
    elif op == "*":
        return a * b
    elif op == "/":
        if b != 0:
            return a / b
        else:
            return "0으로 나눌 수 없습니다."
    else:
        return "유효하지 않은 연산자입니다."
    
