## =======================================================================
## 컨테이너 자료형 - [4] Dict 타입
##
## - 형식 : {키1: 값, 키2: 값. ..., 키n:값}
##          dict({키1: 값, 키2: 값. ..., 키n:값})
##    
## - dict와 내장함수 : len(), max(), min(), sorted()
## ========================================================================
## dict 데이터 생성
## ------------------------------------------------------------------------
## [예시] 6개 과목별 점수를 저장
##       국어 99점, 미술 80점, 음악 100점, 물리 90점, 영어 72점, 수학 81점

jumsuDict1 = {'kor' : 99, 'art' : 80, 'mus' : 100, 'phy' : 90 , 'eng': 72, 'math' : 81}

print(f"jumsuDict1=> 타입 {type(jumsuDict1)}, 원소 개수 : {len(jumsuDict1)}, 데이터 : {jumsuDict1}")

## ==============================================================================================
## 내장함수 max() / min(): ★ 키만 정렬함!! 값은 고려 대상이 아님!!!
## ==============================================================================================
mx, mn = max(jumsuDict1), min(jumsuDict1)

print(f'최대값 : {mx} , 최소값 : {mn}')

## ==============================================================================================
## 내장함수 sorted() : ★ 키만 정렬함!! 값은 고려 대상이 아님!!!
##                    결과는 항상 list 반환
## ==============================================================================================
ret = sorted(jumsuDict1)

print(f"정렬 결과 : {ret}, {type(ret)}")


