# [문제 1]
# 정수 n을 입력받아, n이 양수이면 "양수", 그렇지 않으면 "양수 아님"을 출력하세요.
# 조건부 표현식 한 줄로 작성하세요.

#   n = 7
#   result = ???
#   예상 출력: "양수"
# 
#   n = -3
#   result = ???
#   예상 출력: "양수 아님"
 
[print('양수') if int(input())>0 else print('양수 아님')]
 
 
# [문제 2]
# 정수 n이 짝수이면 "even", 홀수이면 "odd"를 반환하는
# 조건부 표현식을 작성하세요.
# 
#   n = 4  → "even"
#   n = 7  → "odd"

[print('odd') if int(input())%2 else print('even')]
 

# [문제 3]
# 두 수 a, b 중 더 큰 수를 반환하는 조건부 표현식을 작성하세요.
# (내장 함수 max() 사용 금지)
# 
#   a, b = 10, 20  → 20
#   a, b = 35, 12  → 35

num = input().split(",")
a = int(num[0])
b = int(num[1])

[print(a) if a>b else print(b)]

# [문제 4]
# 변수 is_raining이 True이면 "우산을 챙기세요", False이면 "우산 불필요"를
# 출력하는 조건부 표현식을 작성하세요.
# 
#   is_raining = True   → "우산을 챙기세요"
#   is_raining = False  → "우산 불필요"

is_raining = True
[print("우산을 챙기세요") if is_raining else print("우산 불필요")]


# [문제 5]
# 문자열 name이 빈 문자열("")이면 "이름 없음", 아니면 name 값을 그대로
# 반환하는 조건부 표현식을 작성하세요.
# 
#   name = "홍길동"  → "홍길동"
#   name = ""          → "이름 없음"

name = input().strip()
[print(name) if len(name) else print("이름 없음")]


# [문제 6]
# 정수 n이 0이면 "영", 0이 아니면 "0 아님"을 반환하는
# 조건부 표현식을 작성하세요.
#  
#   n = 0   → "영"
#   n = 5   → "0 아님"
#   n = -1  → "0 아님"

n = int(input())
[print("영") if not n else print("0 아님")]
 
 
# [문제 7]
# 어떤 수 n이 10보다 크면 n을 그대로, 10 이하이면 n * 2를 반환하는
# 조건부 표현식을 작성하세요.
#  
#   n = 15  → 15
#   n = 4   → 8
#   n = 10  → 20
 
n = int(input())
[print(n) if n>10 else print(n*2)]
 
# [문제 8]
# 변수 age가 18 이상이면 "성인", 미만이면 "미성년자"를 반환하는
# 조건부 표현식을 작성하세요.
#  
#   age = 20  → "성인"
#   age = 15  → "미성년자"
#   age = 18  → "성인"
  
age = int(input())
[print("성인") if age>=18 else print("미성년자")]
 
# [문제 9]
# 리스트 items가 비어 있으면 "목록이 비었습니다",
# 비어 있지 않으면 items의 첫 번째 원소를 반환하는
# 조건부 표현식을 작성하세요.
#  
#   items = [10, 20, 30]  → 10
#   items = []              → "목록이 비었습니다"

items = [10, 20, 30]
[print(items[0]) if len(items) else print("목록이 비었습니다.")]
  
  
# [문제 10]
# 두 문자열 s1, s2를 비교하여 같으면 "동일", 다르면 "다름"을 반환하는
# 조건부 표현식을 작성하세요.
#  
#   s1, s2 = "python", "python"  → "동일"
#   s1, s2 = "hello", "world"      → "다름"

s = input().split(", ")
s1 = s[0]
s2 = s[1]

[print("동일") if s1 == s2 else print("다름")]
 


