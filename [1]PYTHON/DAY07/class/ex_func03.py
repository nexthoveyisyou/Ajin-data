## =================================================
## 함수기능 : 2개의 정수를 전달받고 덧셈 후 결과 반환
## 함수이름 : addTwo
## 매개변수 : num1, num2
## 함수결과 : total
## =================================================
def addTwo(num1, num2):
    total = num1 + num2
    return total


## =================================================
## 함수기능 : 5개의 정수를 전달받고 덧셈 후 결과 반환
## 함수이름 : addFive
## 매개변수 : num1, num2, num3, num4, num5
## 함수결과 : total
## =================================================
def addFive(num1, num2, num3, num4, num5):
    total = num1 + num2 + num3 + num4 + num5
    return total

