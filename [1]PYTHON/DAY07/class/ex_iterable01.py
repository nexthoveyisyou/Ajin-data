## ===============================================================================
## Iterable 타입 : 반복이 가능한 데이터 타입
##                => for ~ in 반복문으로 요소 추출 가능한 타입들 
##                str, list, tuple, dict, set, bytes, range
##                => 확인 : 메서드에 __iter__( ) 존재
## ===============================================================================

## -------------------------------------------------------------------------------
## iterable 타입 확인
## -------------------------------------------------------------------------------
## for ~ in 반복문에서 _ _ iter_ _(), _ _ next _ _() 사용됨
iterator = [10, 20, 30].__iter__()
iterator = range(1,10,2).__iter__()
iterator = "Good".__iter__()

print(iterator.__next__())
print(iterator.__next__())
print(iterator.__next__())
print(iterator.__next__())
## print(iterator.__next__()) ## StopIteration Error


## -------------------------------------------------------------------------------
## 내장함수 : 반복자를 생성 또는 가져오는 함수 : iter(반복가능한타입)
##           다음 원소 가져오는 내장함수 : next( 반복자 )
## -------------------------------------------------------------------------------
nums = [4,7,-1,3,5]
niterator = iter(nums)

print(next(niterator))
print(next(niterator))

